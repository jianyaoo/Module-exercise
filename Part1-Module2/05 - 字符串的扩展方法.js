// includes()/startsWith()/endsWith()

// 判断字段串是否包含某些内容

let string1 = "hi,my name is Tom";
// includes()
console.log(string1.includes('Tom'));
console.log(string1.includes('july'));

// startsWith()
console.log(string1.startsWith('hi'));
console.log(string1.startsWith('my'));

// endsWith()
console.log(string1.endsWith('Tom'));
console.log(string1.endsWith('is'));

